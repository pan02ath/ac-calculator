# =========================================================
# ENGINE
# =========================================================
def υπολογισμός(d, mode):

    επιφάνεια = d["επιφάνεια"]
    ύψος      = d["ύψος"]
    ΔΤ        = abs(d["εξωτερική"] - d["εσωτερική"])
    όγκος     = επιφάνεια * ύψος

    # --- Wall area (approximate square floor plan) ---
    πλευρά     = math.sqrt(επιφάνεια)
    περίμετρος = 4 * πλευρά
    A_ολοτοίχου = περίμετρος * ύψος

    # --- Glazing area ---
    glazing_factor = ΚΟΥΦΩΜΑΤΑ[d["κουφώματα"]]

    opening_map = [
        ("μεγάλα",       "μεγάλο_παράθυρο"),
        ("μικρά",        "μικρό_παράθυρο"),
        ("μπαλκονόπορτες","διπλή_ανοιγόμενη_μπαλκονόπορτα"),
        ("μονές",        "μονή_ανοιγόμενη_μπαλκονόπορτα"),
        ("συρόμενες",    "διπλή_συρόμενη_μπαλκονόπορτα"),
    ]

    A_ανοιγμάτων_total = sum(
        d[k] * ΑΝΟΙΓΜΑΤΑ[key] for k, key in opening_map
    )

    A_τοίχου_net = max(A_ολοτοίχου - A_ανοιγμάτων_total, 0)

    # --- 1. WALL CONDUCTION (proper U·A·ΔT) ---
    U_wall = U_ΤΟΙΧΟΥ[(d["έτος"], d["μόνωση"])]
    Q_τοίχοι = U_wall * A_τοίχου_net * ΔΤ
    Q_τοίχοι *= ΤΟΙΧΟΙ[d["εξωτερικοί"]]
    Q_τοίχοι *= ΜΗ_ΘΕΡΜΑΙΝΟΜΕΝΟΙ[d["μη_θερμαινόμενοι"]]

    # --- 2. FLOOR ---
    Q_δάπεδο = U_wall * 0.6 * επιφάνεια * ΔΤ * ΔΑΠΕΔΟ[d["δάπεδο"]]

    # --- 3. ROOF ---
    Q_οροφή = 0
    if d["οροφή_υπάρχει"]:
        Q_οροφή = U_wall * 0.8 * επιφάνεια * ΔΤ * ΟΡΟΦΗ[d["οροφή"]]

    # --- 4. OPENINGS (conduction + infiltration) ---
    Q_ανοίγματα   = 0
    Q_διαρροές    = 0
    Q_ηλιακά      = 0
    solar_factor  = ΠΡΟΣΑΝΑΤΟΛΙΣΜΟΣ[d["προσανατολισμός"]]
    shading_factor = ΣΚΙΑΣΗ[d["σκίαση"]]

    for k, key in opening_map:
        count = d[k]
        if count == 0:
            continue
        area = count * ΑΝΟΙΓΜΑΤΑ[key]
        Q_ανοίγματα += area * U_ΑΝΟΙΓΜΑΤΩΝ[key] * glazing_factor * ΔΤ
        Q_διαρροές  += count * ΔΙΑΡΡΟΕΣ[key] * ΑΕΡΟΔΙΕΙΣΔΥΣΗ[d["αεροστεγανότητα"]] * ΔΤ
        Q_ηλιακά    += area * 250 * solar_factor * shading_factor * glazing_factor

    # --- 5. VENTILATION ---
    ach_val     = ACH[d["αερισμός"]]
    Q_αερισμός  = ach_val * όγκος * 0.34 * ΔΤ  # 0.34 W·h/(m³·K)

    # --- 6. INTERNAL LOADS ---
    Q_εσωτερικά = ΕΣΩΤΕΡΙΚΑ[d["τύπος"]]

    # --- 7. LATENT LOAD (cooling only, Mediterranean climate) ---
    latent_factor = 1.12 if mode == "cooling" else 1.0

    # --- TOTAL ---
    if mode == "cooling":
        Q_total = (
            Q_τοίχοι + Q_δάπεδο + Q_οροφή +
            Q_ανοίγματα + Q_διαρροές +
            Q_ηλιακά + Q_αερισμός + Q_εσωτερικά
        ) * latent_factor
    else:
        Q_total = (
            Q_τοίχοι + Q_δάπεδο + Q_οροφή +
            Q_ανοίγματα + Q_διαρροές +
            Q_αερισμός - Q_ηλιακά - Q_εσωτερικά
        )
        Q_total = max(Q_total, 0)

    kw  = Q_total / 1000
    btu = kw * 3412  # 1 kW = 3412 BTU/h

    # --- Snap to nearest standard size (+10% safety margin) ---
    btu_με_περιθώριο = btu * 1.10
    συνιστώμενο_btu  = next(
        (b for b in ΜΕΓΕΘΗ_BTU if b >= btu_με_περιθώριο),
        ΜΕΓΕΘΗ_BTU[-1]
    )
    συνιστώμενο_kw   = συνιστώμενο_btu / 3412

    breakdown = {
        "Τοίχοι":       Q_τοίχοι,
        "Δάπεδο":       Q_δάπεδο,
        "Οροφή":        Q_οροφή,
        "Ανοίγματα":    Q_ανοίγματα,
        "Διαρροές/Αεροδ.": Q_διαρροές,
        "Αερισμός":     Q_αερισμός,
        "Ηλιακά κέρδη": Q_ηλιακά,
        "Εσωτερικά κέρδη": Q_εσωτερικά,
    }

    return kw, btu, συνιστώμενο_kw, συνιστώμενο_btu, breakdown, Q_total

