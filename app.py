# =========================================================
# ENGINE
# =========================================================
def υπολογισμός(d, mode):

    όγκος = d["επιφάνεια"] * d["ύψος"]
    delta_t = d["εξωτερική"] - d["εσωτερική"]
    abs_dt = abs(delta_t)

    # -------------------------
    # BASE
    # -------------------------
    base = όγκος * (22 + abs_dt * 1.0)

    base *= ΕΤΟΣ_ΚΑΤΑΣΚΕΥΗΣ[d["έτος"]]
    base *= ΔΑΠΕΔΟ[d["δάπεδο"]]
    base *= ΕΞΩΤΕΡΙΚΟΙ_ΤΟΙΧΟΙ[d["εξωτερικοί_τοίχοι"]]
    base *= ΜΗ_ΘΕΡΜΑΙΝΟΜΕΝΟΙ[d["μη_θερμαινόμενοι"]]

    if d["έχει_οροφή"]:
        base *= ΟΡΟΦΗ[d["οροφή"]]

    base *= ΑΕΡΟΔΙΕΙΣΔΥΣΗ[d["αεροστεγανότητα"]]

    # -------------------------
    # OPENINGS
    # -------------------------
    u_loss = 0
    infiltration = 0
    solar = 0

    for k, key in [
        ("μεγάλα_παράθυρα", "μεγάλο_παράθυρο"),
        ("μικρά_παράθυρα", "μικρό_παράθυρο"),
        ("μπαλκονόπορτες", "μπαλκονόπορτα"),
        ("μονές_πόρτες", "μονή_πόρτα"),
        ("διπλές_συρόμενες", "διπλή_συρόμενη_μπαλκονόπορτα")
    ]:
        count = d[k]
        area = count * ΑΝΟΙΓΜΑΤΑ[key]

        u_factor = U_ΑΝΟΙΓΜΑΤΩΝ[key]

        # glazing multiplier (FIX #1)
        glazing_factor = ΚΟΥΦΩΜΑΤΑ_ΣΥΝΤΕΛΕΣΤΗΣ[d["κουφώματα"]]

        u_loss += area * u_factor * glazing_factor * abs_dt

        infiltration += count * ΔΙΑΡΡΟΕΣ_ΑΝΑ_ΑΝΟΙΓΜΑ[key] * abs_dt

        # SOLAR NOW ALSO USED IN HEATING (FIX #2)
        solar += area * 220 * d["ηλιος"]

    # -------------------------
    # INTERNAL
    # -------------------------
    internal = ΕΣΩΤΕΡΙΚΑ[d["τύπος"]]

    if mode == "heating":
        base -= internal
        base -= solar * 0.35   # winter solar gain reduces load
    else:
        base += internal
        solar *= 1.0

    total = base + u_loss + infiltration + solar

    kw = total / 1000
    btu = total * 3.412

    return kw, btu


