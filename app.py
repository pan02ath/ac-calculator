def get_commercial_range(nominal_btu_val):
    val_k = nominal_btu_val / 1000
    suitable = [s for s in COMMERCIAL_SIZES if s >= val_k]
    if not suitable:
        return f"{COMMERCIAL_SIZES[-1]}000+"
    idx = COMMERCIAL_SIZES.index(suitable[0])
    return f"{COMMERCIAL_SIZES[max(0, idx-1)]*1000:,} – {COMMERCIAL_SIZES[idx]*1000:,}"


def compute_kenak_zone(nomos: str, altitude_500: bool):
    base = ΝΟΜΟΙ_ΖΩΝΗ.get(nomos, "Β")
    effective = ΖΩΝΗ_ΕΠΟΜΕΝΗ[base] if altitude_500 and base != "Δ" else base
    return base, effective, ΖΩΝΗ_BTU[effective]


# =========================================================
# MAIN ENGINE (EN12831 STRUCTURE CLEANED)
