# =========================================================
# ΥΠΟΛΟΓΙΣΜΟΣ
# =========================================================
def υπολογισμός(d):

    όγκος = d["επιφάνεια"] * d["ύψος"]

    delta_t = max(d["εξωτερική"] - d["εσωτερική"], 5)

    συντελεστής = 30 + (delta_t - 10) * 1.2
    συντελεστής = max(25, συντελεστής)

    βασικό = όγκος * συντελεστής

    # -------------------------
    # ΑΝΟΙΓΜΑΤΑ
    # -------------------------
    παράθυρα = (
        d["μεγάλα_παράθυρα"] * ΑΝΟΙΓΜΑΤΑ["μεγάλο_παράθυρο"] +
        d["μικρά_παράθυρα"] * ΑΝΟΙΓΜΑΤΑ["μικρό_παράθυρο"] +
        d["μπαλκονόπορτες"] * ΑΝΟΙΓΜΑΤΑ["μπαλκονόπορτα"]
    )

    πόρτες = (
        d["μονές_πόρτες"] * ΑΝΟΙΓΜΑΤΑ["μονή_πόρτα"] +
        d["διπλές_συρόμενες"] * ΑΝΟΙΓΜΑΤΑ["διπλή_συρόμενη_μπαλκονόπορτα"]
    )

    συνολικά_ανοίγματα = παράθυρα + πόρτες

    # -------------------------
    # ΗΛΙΑΚΑ
    # -------------------------
    ηλιακά = συνολικά_ανοίγματα * 260
    ηλιακά *= ΗΛΙΟΣ[d["ήλιος"]]
    ηλιακά *= ΚΟΥΦΩΜΑΤΑ[d["κουφώματα"]]
    ηλιακά *= ΠΡΟΣΑΝΑΤΟΛΙΣΜΟΣ[d["προσανατολισμός"]]

    # -------------------------
    # ΚΤΙΡΙΟ
    # -------------------------
    βασικό *= ΕΤΟΣ_ΚΑΤΑΣΚΕΥΗΣ[d["έτος"]]
    βασικό *= ΔΑΠΕΔΟ_ΕΠΑΦΗ[d["δάπεδο_επαφή"]]
    βασικό *= ΕΞΩΤΕΡΙΚΟΙ_ΤΟΙΧΟΙ[d["εξωτερικοί_τοίχοι"]]
    βασικό *= ΤΟΙΧΟΙ_ΜΗ_ΘΕΡΜΑΙΝΟΜΕΝΟΙ[d["μη_θερμαινόμενοι_τοίχοι"]]

    # -------------------------
    # ΟΡΟΦΗ
    # -------------------------
    if d["έχει_οροφή"]:
        βασικό *= ΟΡΟΦΗ_ΠΑΝΩ[d["πάνω_από_οροφή"]]

    # -------------------------
    # ΕΣΩΤΕΡΙΚΑ ΦΟΡΤΙΑ
    # -------------------------
    βασικό *= ΕΣΩΤΕΡΙΚΑ_ΦΟΡΤΙΑ[d["τύπος_δωματίου"]]

    # -------------------------
    # ΥΓΡΑΣΙΑ
    # -------------------------
    βασικό *= d["υγρασία"]

    συνολικό = βασικό + ηλιακά

    kw = συνολικό / 1000
    btu = συνολικό * 3.412

    return kw, btu, συνολικά_ανοίγματα

