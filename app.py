import streamlit as st

# =========================================================
# OPENINGS (STANDARD SIZES)
# =========================================================
ΑΝΟΙΓΜΑΤΑ = {
    "μονή_πόρτα": 0.90 * 2.10,
    "διπλή_συρόμενη_μπαλκονόπορτα": 1.80 * 2.10,
    "μπαλκονόπορτα": 1.60 * 2.10,
    "μεγάλο_παράθυρο": 1.20 * 1.40,
    "μικρό_παράθυρο": 0.60 * 0.60,
}

# =========================================================
# U-VALUES (OPENINGS)
# =========================================================
U_ΑΝΟΙΓΜΑΤΩΝ = {
    "μονή_πόρτα": 3.0,
    "διπλή_συρόμενη_μπαλκονόπορτα": 2.6,
    "μπαλκονόπορτα": 2.8,
    "μεγάλο_παράθυρο": 2.2,
    "μικρό_παράθυρο": 2.4,
}

# =========================================================
# INFILTRATION PER OPENING
# =========================================================
ΔΙΑΡΡΟΕΣ_ΑΝΑ_ΑΝΟΙΓΜΑ = {
    "μονή_πόρτα": 25,
    "διπλή_συρόμενη_μπαλκονόπορτα": 15,
    "μπαλκονόπορτα": 18,
    "μεγάλο_παράθυρο": 8,
    "μικρό_παράθυρο": 5,
}

# =========================================================
# BUILDING FACTORS
# =========================================================
ΕΤΟΣ_ΚΑΤΑΣΚΕΥΗΣ = {
    "πριν_1980": 1.30,
    "1980_2000": 1.12,
    "2000_2010": 1.0,
    "2010_σήμερα": 0.85
}

ΔΑΠΕΔΟ = {
    "πιλοτή": 1.15,
    "άλλο_διαμέρισμα": 0.90,
    "μη_θερμαινόμενος_χώρος": 1.05,
    "έδαφος": 1.10
}

ΟΡΟΦΗ = {
    "ταράτσα_εκτεθειμένη": 1.25,
    "μονωμένη": 1.0,
    "κεραμοσκεπή": 1.15,
    "θερμαινόμενος_χώρος": 0.90
}

ΕΞΩΤΕΡΙΚΟΙ_ΤΟΙΧΟΙ = {
    1: 0.95,
    2: 1.0,
    3: 1.1,
    4: 1.2
}

ΜΗ_ΘΕΡΜΑΙΝΟΜΕΝΟΙ = {
    0: 1.0,
    1: 1.07,
    2: 1.14
}

ΚΟΥΦΩΜΑΤΑ = {
    "αλουμίνιο_χωρίς_θερμοδιακοπή": 1.25,
    "αλουμίνιο_θερμοδιακοπή": 1.05,
    "pvc": 0.90,
    "low_e": 0.75
}

ΑΕΡΟΔΙΕΙΣΔΥΣΗ = {
    "κακή": 1.25,
    "μέτρια": 1.0,
    "καλή": 0.85
}

ΗΛΙΟΣ = {
    "σκιασμένο": 0.85,
    "κανονικό": 1.0,
    "έντονη": 1.2
}

ΕΣΩΤΕΡΙΚΑ = {
    "υπνοδωμάτιο": 350,
    "σαλόνι": 500,
    "κουζίνα": 600,
    "γραφείο": 400
}

# =========================================================
# ENGINE
# =========================================================
def υπολογισμός(d, mode):

    όγκος = d["επιφάνεια"] * d["ύψος"]
    delta_t = d["εξωτερική"] - d["εσωτερική"]
    abs_dt = abs(delta_t)

    # -------------------------
    # BASE LOAD
    # -------------------------
    base = όγκος * (22 + abs_dt * 1.0)

    base *= ΕΤΟΣ_ΚΑΤΑΣΚΕΥΗΣ[d["έτος"]]
    base *= ΔΑΠΕΔΟ[d["δάπεδο"]]
    base *= ΕΞΩΤΕΡΙΚΟΙ_ΤΟΙΧΟΙ[d["εξωτερικοί_τοίχοι"]]
    base *= ΜΗ_ΘΕΡΜΑΙΝΟΜΕΝΟΙ[d["μη_θερμαινόμενοι"]]

    if d["έχει_οροφή"]:
        base *= ΟΡΟΦΗ[d["οροφή"]]

    base *= ΑΕΡΟΔΙΕΙΣΔΥΣΗ[d["αεροστεγανότητα"]]

    # -------------------------
    # OPENINGS
    # -------------------------
    u_loss_openings = 0
    infiltration = 0
    solar = 0

    for k in ["μεγάλα_παράθυρα", "μικρά_παράθυρα", "μπαλκονόπορτες", "μονές_πόρτες", "διπλές_συρόμενες"]:
        if k == "μεγάλα_παράθυρα":
            area_key = "μεγάλο_παράθυρο"
        elif k == "μικρά_παράθυρα":
            area_key = "μικρό_παράθυρο"
        elif k == "μπαλκονόπορτες":
            area_key = "μπαλκονόπορτα"
        elif k == "μονές_πόρτες":
            area_key = "μονή_πόρτα"
        else:
            area_key = "διπλή_συρόμενη_μπαλκονόπορτα"

        count = d[k]

        area = count * ΑΝΟΙΓΜΑΤΑ[area_key]

        u_loss_openings += area * U_ΑΝΟΙΓΜΑΤΩΝ[area_key] * abs_dt

        infiltration += count * ΔΙΑΡΡΟΕΣ_ΑΝΑ_ΑΝΟΙΓΜΑ[area_key] * abs_dt

        if mode == "cooling":
            solar += area * 240 * d["ηλιος"]

    # -------------------------
    # INTERNAL LOADS
    # -------------------------
    internal = ΕΣΩΤΕΡΙΚΑ[d["τύπος"]]

    if mode == "heating":
        base -= internal
    else:
        base += internal

    total = base + u_loss_openings + infiltration + solar

    kw = total / 1000
    btu = total * 3.412

    return kw, btu


# =========================================================
# UI
# =========================================================
st.title("Ενιαίος Υπολογιστής Ψύξης / Θέρμανσης")

mode = st.radio("Λειτουργία", ["cooling", "heating"])

st.header("1. Κτίριο")
έτος = st.selectbox("Έτος κατασκευής", list(ΕΤΟΣ_ΚΑΤΑΣΚΕΥΗΣ.keys()))

st.header("2. Διαστάσεις")
επιφάνεια = st.number_input("Επιφάνεια (m²)", 5, 200, 20, step=1)
ύψος = st.number_input("Ύψος (m)", 2.0, 4.5, 2.8)

st.header("3. Θερμοκρασίες")
εσωτερική = st.number_input("Εσωτερική °C", 18, 28 if mode=="cooling" else 24, 24 if mode=="cooling" else 20)
εξωτερική = st.number_input("Εξωτερική °C", 30 if mode=="cooling" else -5, 45 if mode=="cooling" else 15, 35 if mode=="cooling" else 5)

st.header("4. Δωμάτιο")
τύπος = st.selectbox("Χρήση", list(ΕΣΩΤΕΡΙΚΑ.keys()))

st.header("5. Κέλυφος")
δάπεδο = st.selectbox("Δάπεδο", list(ΔΑΠΕΔΟ.keys()))
εξωτερικοί_τοίχοι = st.selectbox("Εξωτερικοί τοίχοι", list(ΕΞΩΤΕΡΙΚΟΙ_ΤΟΙΧΟΙ.keys()))
μη_θερμαινόμενοι = st.selectbox("Μη θερμαινόμενοι τοίχοι", list(ΜΗ_ΘΕΡΜΑΙΝΟΜΕΝΟΙ.keys()))

έχει_οροφή = st.selectbox("Οροφή;", ["όχι", "ναι"]) == "ναι"
οροφή = "μονωμένη"
if έχει_οροφή:
    οροφή = st.selectbox("Τύπος οροφής", list(ΟΡΟΦΗ.keys()))

κουφώματα = st.selectbox("Κουφώματα", list(ΚΟΥΦΩΜΑΤΑ.keys()))
αεροστεγανότητα = st.selectbox("Αεροστεγανότητα", list(ΑΕΡΟΔΙΕΙΣΔΥΣΗ.keys()))

st.header("6. Ανοίγματα")

μεγάλα_παράθυρα = st.number_input("Μεγάλα παράθυρα", 0, 20, 2)
μικρά_παράθυρα = st.number_input("Μικρά παράθυρα", 0, 20, 1)
μπαλκονόπορτες = st.number_input("Μπαλκονόπορτες", 0, 10, 0)
μονές_πόρτες = st.number_input("Μονές πόρτες", 0, 5, 1)
διπλές_συρόμενες = st.number_input("Διπλή συρόμενη μπαλκονόπορτα", 0, 5, 0)

st.header("7. Ήλιος")
ηλιος = st.selectbox("Έκθεση", list(ΗΛΙΟΣ.keys()))

# =========================================================
# DATA
# =========================================================
d = {
    "έτος": έτος,
    "επιφάνεια": επιφάνεια,
    "ύψος": ύψος,
    "εσωτερική": εσωτερική,
    "εξωτερική": εξωτερική,
    "τύπος": τύπος,
    "δάπεδο": δάπεδο,
    "εξωτερικοί_τοίχοι": εξωτερικοί_τοίχοι,
    "μη_θερμαινόμενοι": μη_θερμαινόμενοι,
    "έχει_οροφή": έχει_οροφή,
    "οροφή": οροφή,
    "κουφώματα": κουφώματα,
    "αεροστεγανότητα": αεροστεγανότητα,
    "μεγάλα_παράθυρα": μεγάλα_παράθυρα,
    "μικρά_παράθυρα": μικρά_παράθυρα,
    "μπαλκονόπορτες": μπαλκονόπορτες,
    "μονές_πόρτες": μονές_πόρτες,
    "διπλές_συρόμενες": διπλές_συρόμενες,
    "ηλιος": ΗΛΙΟΣ[ηλιος]
}

# =========================================================
# OUTPUT
# =========================================================
if st.button("Υπολογισμός"):

    kw, btu = υπολογισμός(d, mode)

    st.subheader("Αποτέλεσμα")

    if mode == "cooling":
        st.write("❄️ Ψυκτικό φορτίο")
    else:
        st.write("🔥 Θερμικό φορτίο")

    st.write(f"{kw:.2f} kW")
    st.write(f"{btu:.0f} BTU/h")

    if kw < 2.5:
        st.success("Προτεινόμενο: ~9.000 BTU")
    elif kw < 3.5:
        st.success("Προτεινόμενο: ~12.000 BTU")
    elif kw < 5:
        st.success("Προτεινόμενο: ~18.000 BTU")
    else:
        st.success("Προτεινόμενο: 24.000+ BTU")
