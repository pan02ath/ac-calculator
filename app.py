# -*- coding: utf-8 -*-
import streamlit as st
from utils import *

# =========================================================
# UI
# =========================================================
st.set_page_config(page_title="Υπολογιστής Ενεργειακών Απαιτήσεων HVAC", layout="centered")
st.title("Υπολογιστής ενεργειακών απαιτήσεων για κλιματισμό")

mode = st.radio("Λειτουργία", ["ψύξη", "θέρμανση"])

if "last_mode" not in st.session_state or st.session_state.last_mode != mode:
    st.session_state.last_mode = mode
    if mode == "ψύξη":
        st.session_state.tin, st.session_state.tout = 24, 38
    else:
        st.session_state.tin, st.session_state.tout = 21, 5

st.header("Κλιματικές συνθήκες")
c1, c2 = st.columns(2)

with c1:
    εσωτερική = st.number_input("Εσωτερική Θερμοκρασία (°C)", value=st.session_state.tin)
    εξωτερική = st.number_input("Εξωτερική Θερμοκρασία (°C)", value=st.session_state.tout)

ηλιακή_έκθεση_val, ηλιακή_έκθεση_label = 1.0, "N/A"
βάση_val, kenak_zone_label = 210, "N/A"
νομός, υψόμετρο_500 = "N/A", False
base_zone, effective_zone = "N/A", "N/A"

if mode == "ψύξη":
    with c2:
        νομός = st.selectbox(
            "Νομός / Περιοχή",
            list(ΝΟΜΟΙ_ΖΩΝΗ.keys()),
            help="Επιλέξτε τον νομό ή την περιοχή του ακινήτου."
        )
        υψόμετρο_500 = st.checkbox(
            "Υψόμετρο > 500 μέτρα",
            help=(
                "Σύμφωνα με τον ΚΕΝΑΚ, περιοχές με υψόμετρο άνω των 500 μ. "
                "εντάσσονται στην επόμενη ψυχρότερη κλιματική ζώνη "
                "(π.χ. Ζώνη Β → Ζώνη Γ). Η Ζώνη Δ παραμένει Δ ανεξαρτήτως υψομέτρου."
            )
        )
        base_zone, effective_zone, βάση_val = compute_kenak_zone(νομός, υψόμετρο_500)
        kenak_zone_label = f"Ζώνη {effective_zone}"
        ηλιακή_έκθεση_label = st.selectbox("Ηλιακή έκθεση", list(ΗΛΙΑΚΗ_ΕΚΘΕΣΗ.keys()))
        ηλιακή_έκθεση_val = ΗΛΙΑΚΗ_ΕΚΘΕΣΗ[ηλιακή_έκθεση_label]
else:
    with c2:
        st.info("Σε λειτουργία θέρμανσης, τα ηλιακά κέρδη παραλείπονται για τον υπολογισμό φορτίου αιχμής.")

st.header("Περιγραφή χώρου")
c3, c4 = st.columns(2)
with c3:
    επιφάνεια = st.number_input("Επιφάνεια χώρου (m²)", 5, 200, 20)
    ύψος = st.number_input("Ύψος χώρου (m)", 2.0, 4.5, 2.8)
    έτος = st.selectbox("Έτος κατασκευής", list(ΕΤΟΣ.keys()))
with c4:
    τύπος = st.selectbox("Χρήση χώρου", list(ΕΣΩΤΕΡΙΚΑ.keys()))
    δάπεδο = st.selectbox("Δάπεδο σε επαφή με", list(ΔΑΠΕΔΟ.keys()))
    βόρειος = st.checkbox("Βόρειος προσανατολισμός") if mode == "θέρμανση" else False

st.header("Δομικά στοιχεία")
μόνωση = st.selectbox("Θερμομόνωση τοίχων", list(ΘΕΡΜΟΜΟΝΩΣΗ.keys()))
c5, c6 = st.columns(2)
with c5:
    εξωτερικοί = st.selectbox("Πλήθος εξωτερικών τοίχων", list(ΤΟΙΧΟΙ.keys()))
    μη_θερμαινόμενοι = st.selectbox("Τοίχοι σε επαφή με μη θερμαινόμενους χώρους", list(ΜΗ_ΘΕΡΜΑΙΝΟΜΕΝΟΙ.keys()))
    αεροστεγανότητα = st.selectbox("Ποιότητα αεροστεγανότητας", list(ΑΕΡΟΔΙΕΙΣΔΥΣΗ.keys()))
with c6:
    οροφή_υπάρχει = st.selectbox("Ύπαρξη οροφής", ["όχι", "ναι"]) == "ναι"
    οροφή = st.selectbox("Τύπος οροφής", list(ΟΡΟΦΗ.keys())) if οροφή_υπάρχει else "N/A"
    κουφώματα = st.selectbox("Τύπος πλαισίων/τζαμιών", list(ΚΟΥΦΩΜΑΤΑ.keys()))

st.write("**Πλήθος Ανοιγμάτων:**")
c7, c8, c9 = st.columns(3)
with c7:
    μεγάλα = st.number_input("Μεγάλα παράθυρα", 0, 20, 0)
    μονές = st.number_input("Μονές μπαλκ/πορτες", 0, 10, 0)
with c8:
    μικρά = st.number_input("Μικρά παράθυρα", 0, 20, 0)
    συρόμενες = st.number_input("Συρόμενες μπαλκ/πορτες", 0, 10, 0)
with c9:
    μπαλκονόπορτες = st.number_input("Διπλές ανοιγόμενες μπαλκ/πορτες", 0, 10, 1)

st.header("Συνήθης χρήση κλιματιστικού")
περιστασιακή = st.checkbox(
    "Προτιμάτε περιστασιακή χρήση;",
    help="Αν ο χώρος θερμαίνεται/ψύχεται μόνο κατά διαστήματα, η μονάδα πρέπει να καλύπτει γρήγορη ανάκτηση."
)
αθόρυβη = st.checkbox(
    "Προτιμάτε αθόρυβη λειτουργία (χαμηλή ταχύτητα ανεμιστήρα);",
    help="Η λειτουργία σε χαμηλή ταχύτητα μειώνει την πραγματική απόδοση — χρειάζεται μεγαλύτερη μονάδα."
)

d = {
    "έτος": έτος, "μόνωση": μόνωση, "επιφάνεια": επιφάνεια, "ύψος": ύψος,
    "εσωτερική": εσωτερική, "εξωτερική": εξωτερική, "τύπος": τύπος,
    "δάπεδο": δάπεδο, "εξωτερικοί": εξωτερικοί, "μη_θερμαινόμενοι": μη_θερμαινόμενοι,
    "οροφή_υπάρχει": οροφή_υπάρχει, "οροφή": οροφή, "κουφώματα": κουφώματα,
    "αεροστεγανότητα": αεροστεγανότητα, "μεγάλα": μεγάλα, "μικρά": μικρά,
    "μπαλκονόπορτες": μπαλκονόπορτες, "μονές": μονές, "συρόμενες": συρόμενες,
    "ηλιακή_έκθεση": ηλιακή_έκθεση_val, "βάση_ακτινοβολίας": βάση_val,
    "βόρειος": βόρειος, "περιστασιακή": περιστασιακή, "αθόρυβη": αθόρυβη,
    "kenak_label": kenak_zone_label,
}

if st.button("Υπολογισμός"):
    try:
        kw, load_btu, nominal_btu_base, nominal_btu_final, unit_penalty_factors, breakdown, f_derating = υπολογισμός(d, mode)
        commercial_range_base  = get_commercial_range(nominal_btu_base)
        commercial_range_final = get_commercial_range(nominal_btu_final)

        # ── Save inputs for the Load Profile page ──────────────────────────
        st.session_state["hvac_inputs"] = d

        st.divider()

        st.success(f"**Φορτίο αιχμής:** {kw:.2f} kW | {load_btu:.0f} BTU/h")
        st.info(f"**Εύρος απωλειών (±15%):** {load_btu*0.85:.0f} – {load_btu*1.15:.0f} BTU/h")

        st.subheader("Ανάλυση φορτίου αιχμής")
        for label, watts in breakdown.items():
            if watts == 0:
                continue
            st.write(f"**{label}:** {watts/1000:.2f} kW")

        st.divider()
        st.subheader("Επιλογή μεγέθους κλιματιστικού")

        derating_pct = (1 - f_derating) * 100
        st.write(
            f"Λόγω μειωμένης απόδοσης κλιματιστικού στους **{εξωτερική}°C** "
            f"(−{derating_pct:.0f}%), η απαιτούμενη **ονομαστική** ισχύς ανέρχεται σε "
            f"**{nominal_btu_base:,.0f} BTU/h**."
        )

        if unit_penalty_factors:
            st.info(f"**Βασική σύσταση (χωρίς προτιμήσεις χρήσης):** {commercial_range_base} BTU")
            st.warning("**Προσαυξήσεις λόγω προτιμήσεων χρήσης** *(επηρεάζουν μόνο το μέγεθος μονάδας, όχι τις απώλειες)*:")
            for reason, factor in unit_penalty_factors.items():
                explanation = {
                    "Περιστασιακή χρήση": (
                        f"Η μονάδα πρέπει να επιτυγχάνει ταχύτερα την επιθυμητή θερμοκρασία "
                        f"σε χώρο που δεν κλιματιζόταν προηγουμένως. → ×{factor:.2f}"
                    ),
                    "Αθόρυβη/χαμηλή ταχύτητα": (
                        f"Σε χαμηλή ταχύτητα η απόδοση μειώνεται. "
                        f"Συνιστάται μονάδα μεγαλύτερης ισχύος. → ×{factor:.2f}"
                    ),
                }.get(reason, f"×{factor:.2f}")
                st.write(f"- **{reason}:** {explanation}")

            total_factor = 1.0
            for f in unit_penalty_factors.values():
                total_factor *= f
            st.success(
                f"**Συνιστώμενη ονομαστική ισχύς μονάδας (με προτιμήσεις):** "
                f"**{commercial_range_final} BTU** "
                f"*(συνολική προσαύξηση ×{total_factor:.2f})*"
            )
        else:
            st.success(f"**Συνιστώμενη ονομαστική ισχύς μονάδας:** **{commercial_range_base} BTU**")

        altitude_note = (
            f"Υψόμετρο >500 μ. (αναβάθμιση ζώνης: {base_zone} → {effective_zone})"
            if υψόμετρο_500 and base_zone != effective_zone
            else ("Υψόμετρο >500 μ. (ζώνη παραμένει Δ)" if υψόμετρο_500 else "Όχι")
        )

        unit_penalty_lines = "\n".join(
            f"  - {r}: ×{f:.2f}" for r, f in unit_penalty_factors.items()
        ) or "  Καμία"

        report = f"""ΑΝΑΦΟΡΑ ΕΝΕΡΓΕΙΑΚΩΝ ΑΠΑΙΤΗΣΕΩΝ (Λειτουργία: {mode.upper()})
--------------------------------------------------
ΚΛΙΜΑΤΙΚΕΣ ΣΥΝΘΗΚΕΣ:
- Θερμοκρασία: Εσωτ. {εσωτερική}°C / Εξωτ. {εξωτερική}°C
- Νομός / Περιοχή: {νομός}
- Υψόμετρο >500 μ.: {altitude_note}
- Ζώνη ΚΕΝΑΚ: {kenak_zone_label}
- Ηλιακή Έκθεση: {ηλιακή_έκθεση_label}

ΠΕΡΙΓΡΑΦΗ ΧΩΡΟΥ:
- Χώρος: {τύπος} ({επιφάνεια}m² / {ύψος}m ύψος)
- Έτος Κατασκευής: {έτος}
- Βόρειος Προσανατολισμός: {"Ναι (+15% στις απώλειες)" if βόρειος else "Όχι"}

ΔΟΜΙΚΑ ΣΤΟΙΧΕΙΑ:
- Εξωτερικοί Τοίχοι (Πλήθος): {εξωτερικοί}
- Τοίχοι σε επαφή με μη θερμαινόμενους χώρους: {μη_θερμαινόμενοι}
- Μόνωση Τοίχων: {μόνωση}
- Κουφώματα: {κουφώματα}
- Αεροστεγανότητα: {αεροστεγανότητα}
- Δάπεδο: {δάπεδο}
- Οροφή: {οροφή if οροφή_υπάρχει else "Δεν υπάρχει / Θερμαινόμενος χώρος"}
- Παράθυρα: {μεγάλα} Μεγάλα / {μικρά} Μικρά
- Μπαλκονόπορτες: {μπαλκονόπορτες} Διπλές / {μονές} Μονές / {συρόμενες} Συρόμενες

--------------------------------------------------
ΑΠΟΤΕΛΕΣΜΑΤΑ

Φορτίο αιχμής (απώλειες): {load_btu:.0f} BTU/h ({kw:.2f} kW)
Μείωση απόδοσης μονάδας στους {εξωτερική}°C: -{derating_pct:.0f}%
Βασική ονομαστική ισχύς (χωρίς προτιμήσεις χρήσεως): {nominal_btu_base:,.0f} BTU/h → {commercial_range_base} BTU

Προσαυξήσεις λόγω προτιμήσεων χρήσης:
{unit_penalty_lines}
Τελική ονομαστική ισχύς (με προτιμήσεις χρήσεως): {nominal_btu_final:,.0f} BTU/h → {commercial_range_final} BTU
--------------------------------------------------"""
        st.code(report, language="text")

        st.markdown("---")
        st.caption(
            "**Αποποίηση Ευθύνης:** Ο παρών υπολογισμός αποτελεί εκτίμηση. "
            "Για την οριστική μελέτη, απαιτείται αυτοψία από αδειούχο μηχανολόγο."
        )
    except Exception as e:
        st.error(f"Σφάλμα: {e}")
