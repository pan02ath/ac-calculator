import streamlit as st

# =========================================================
# OPENINGS
# =========================================================
ΑΝΟΙΓΜΑΤΑ = {
    "μονή_πόρτα": 0.90 * 2.10,
    "διπλή_συρόμενη_μπαλκονόπορτα": 1.80 * 2.10,
    "μπαλκονόπορτα": 1.60 * 2.10,
    "μεγάλο_παράθυρο": 1.20 * 1.40,
    "μικρό_παράθυρο": 0.60 * 0.60,
}

# =========================================================
# U VALUES
# =========================================================
U_ΑΝΟΙΓΜΑΤΩΝ = {
    "μονή_πόρτα": 3.2,
    "διπλή_συρόμενη_μπαλκονόπορτα": 2.8,
    "μπαλκονόπορτα": 3.0,
    "μεγάλο_παράθυρο": 2.5,
    "μικρό_παράθυρο": 2.7,
}

# glazing multiplier
ΚΟΥΦΩΜΑΤΑ = {
    "αλουμίνιο_χωρίς_θερμοδιακοπή": 1.30,
    "αλουμίνιο_θερμοδιακοπή": 1.10,
    "pvc": 0.90,
    "low_e": 0.75,
    "μονό_τζάμι": 1.65
}

# =========================================================
# INFILTRATION (NOW DEPENDS ON WINDOWS)
# =========================================================
ΔΙΑΡΡΟΕΣ = {
    "μονή_πόρτα": 30,
    "διπλή_συρόμενη_μπαλκονόπορτα": 20,
    "μπαλκονόπορτα": 22,
    "μεγάλο_παράθυρο": 10,
    "μικρό_παράθυρο": 6,
}

ΑΕΡΟΔΙΕΙΣΔΥΣΗ = {
    "κακή": 1.3,
    "μέτρια": 1.0,
    "καλή": 0.75
}

# =========================================================
# BUILDING
# =========================================================
ΕΤΟΣ = {
    "πριν_1980": 1.30,
    "1980_2000": 1.12,
    "2000_2010": 1.0,
    "2010_σήμερα": 0.85
}

ΔΑΠΕΔΟ = {
    "πιλοτή": 1.15,
    "άλλο_διαμέρισμα": 0.90,
    "μη_θερμαινόμενος_χώρος": 1.05,
    "έδαφος": 1.10
}

ΟΡΟΦΗ = {
    "ταράτσα_εκτεθειμένη": 1.25,
    "μονωμένη": 1.0,
    "κεραμοσκεπή": 1.15,
    "θερμαινόμενος_χώρος": 0.90
}

ΤΟΙΧΟΙ = {1:0.95, 2:1.0, 3:1.1, 4:1.2}
ΜΗ_ΘΕΡΜΑΙΝΟΜΕΝΟΙ = {0:1.0, 1:1.07, 2:1.14}

ΗΛΙΟΣ = {
    "σκιασμένο": 0.7,
    "κανονικό": 1.0,
    "έντονη": 1.25
}

ΕΣΩΤΕΡΙΚΑ = {
    "υπνοδωμάτιο": 300,
    "σαλόνι": 500,
    "κουζίνα": 700,
    "γραφείο": 400
}

# =========================================================
# ENGINE (FIXED)
# =========================================================
def υπολογισμός(d, mode):

    όγκος = d["επιφάνεια"] * d["ύψος"]
    ΔΤ = abs(d["εξωτερική"] - d["εσωτερική"])

    # -------------------------
    # BASE (walls etc)
    # -------------------------
    base = όγκος * (18 + ΔΤ * 1.1)

    base *= ΕΤΟΣ[d["έτος"]]
    base *= ΔΑΠΕΔΟ[d["δάπεδο"]]
    base *= ΤΟΙΧΟΙ[d["εξωτερικοί"]]
    base *= ΜΗ_ΘΕΡΜΑΙΝΟΜΕΝΟΙ[d["μη_θερμαινόμενοι"]]

    if d["οροφή_υπάρχει"]:
        base *= ΟΡΟΦΗ[d["οροφή"]]

    base *= ΑΕΡΟΔΙΕΙΣΔΥΣΗ[d["αεροστεγανότητα"]]

    # -------------------------
    # OPENINGS
    # -------------------------
    u_loss = 0
    infiltration = 0
    solar = 0

    glazing = ΚΟΥΦΩΜΑΤΑ[d["κουφώματα"]]

    for k, key in [
        ("μεγάλα", "μεγάλο_παράθυρο"),
        ("μικρά", "μικρό_παράθυρο"),
        ("μπαλκονόπορτες", "μπαλκονόπορτα"),
        ("μονές", "μονή_πόρτα"),
        ("συρόμενες", "διπλή_συρόμενη_μπαλκονόπορτα")
    ]:

        count = d[k]
        area = count * ΑΝΟΙΓΜΑΤΑ[key]

        # conduction
        u_loss += area * U_ΑΝΟΙΓΜΑΤΩΝ[key] * glazing * ΔΤ

        # infiltration now depends ALSO on glazing
        infiltration += count * ΔΙΑΡΡΟΕΣ[key] * glazing * ΔΤ

        # solar
        solar += area * 200 * d["ήλιος"]

    # -------------------------
    # INTERNAL
    # -------------------------
    internal = ΕΣΩΤΕΡΙΚΑ[d["τύπος"]]

    # -------------------------
    # FINAL LOGIC (FIXED)
    # -------------------------
    if mode == "heating":
        total = base + u_loss + infiltration - solar - internal
    else:
        total = base + u_loss + infiltration + solar + internal

    kw = total / 1000
    btu = total * 3.412

    return kw, btu


# =========================================================
# UI
# =========================================================
st.title("Υπολογιστής HVAC")

mode = st.radio("Λειτουργία", ["cooling", "heating"])

έτος = st.selectbox("Έτος", list(ΕΤΟΣ.keys()))

επιφάνεια = st.number_input("m²", 5, 200, 20)
ύψος = st.number_input("Ύψος", 2.0, 4.5, 2.8)

εσωτερική = st.number_input("Εσωτερική", 18, 28, 24)
εξωτερική = st.number_input("Εξωτερική", -5, 45, 35)

τύπος = st.selectbox("Χρήση", list(ΕΣΩΤΕΡΙΚΑ.keys()))

δάπεδο = st.selectbox("Δάπεδο", list(ΔΑΠΕΔΟ.keys()))
εξωτερικοί = st.selectbox("Εξωτερικοί τοίχοι", list(ΤΟΙΧΟΙ.keys()))
μη_θερμαινόμενοι = st.selectbox("Μη θερμαινόμενοι", list(ΜΗ_ΘΕΡΜΑΙΝΟΜΕΝΟΙ.keys()))

οροφή_υπάρχει = st.selectbox("Οροφή", ["όχι", "ναι"]) == "ναι"
οροφή = "μονωμένη"
if οροφή_υπάρχει:
    οροφή = st.selectbox("Τύπος οροφής", list(ΟΡΟΦΗ.keys()))

κουφώματα = st.selectbox("Κουφώματα", list(ΚΟΥΦΩΜΑΤΑ.keys()))
αεροστεγανότητα = st.selectbox("Αεροστεγανότητα", list(ΑΕΡΟΔΙΕΙΣΔΥΣΗ.keys()))

μεγάλα = st.number_input("Μεγάλα παράθυρα", 0, 20, 2)
μικρά = st.number_input("Μικρά παράθυρα", 0, 20, 1)
μπαλκονόπορτες = st.number_input("Μπαλκονόπορτες", 0, 10, 0)
μονές = st.number_input("Μονές πόρτες", 0, 5, 1)
συρόμενες = st.number_input("Διπλές συρόμενες", 0, 5, 0)

ήλιος = st.selectbox("Ήλιος", list(ΗΛΙΟΣ.keys()))

d = {
    "έτος": έτος,
    "επιφάνεια": επιφάνεια,
    "ύψος": ύψος,
    "εσωτερική": εσωτερική,
    "εξωτερική": εξωτερική,
    "τύπος": τύπος,
    "δάπεδο": δάπεδο,
    "εξωτερικοί": εξωτερικοί,
    "μη_θερμαινόμενοι": μη_θερμαινόμενοι,
    "οροφή_υπάρχει": οροφή_υπάρχει,
    "οροφή": οροφή,
    "κουφώματα": κουφώματα,
    "αεροστεγανότητα": αεροστεγανότητα,
    "μεγάλα": μεγάλα,
    "μικρά": μικρά,
    "μπαλκονόπορτες": μπαλκονόπορτες,
    "μονές": μονές,
    "συρόμενες": συρόμενες,
    "ήλιος": ΗΛΙΟΣ[ήλιος]
}

if st.button("Υπολογισμός"):
    kw, btu = υπολογισμός(d, mode)
    st.write(f"{kw:.2f} kW")
    st.write(f"{btu:.0f} BTU/h")
