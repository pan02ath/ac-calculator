# =========================================================
# CORE ENGINE (UNIFIED)
# =========================================================
def υπολογισμός(d, mode):

    όγκος = d["επιφάνεια"] * d["ύψος"]

    delta_t = d["εξωτερική"] - d["εσωτερική"]
    abs_dt = abs(delta_t)

    # =========================
    # BASE WALL LOAD
    # =========================
    base = όγκος * (22 + abs_dt * 1.0)

    base *= ΕΤΟΣ_ΚΑΤΑΣΚΕΥΗΣ[d["έτος"]]
    base *= ΔΑΠΕΔΟ[d["δάπεδο"]]
    base *= ΕΞΩΤΕΡΙΚΟΙ_ΤΟΙΧΟΙ[d["εξωτερικοί_τοίχοι"]]
    base *= ΜΗ_ΘΕΡΜΑΙΝΟΜΕΝΟΙ[d["μη_θερμαινόμενοι"]]

    if d["έχει_οροφή"]:
        base *= ΟΡΟΦΗ[d["οροφή"]]

    base *= ΑΕΡΟΔΙΕΙΣΔΥΣΗ[d["αεροστεγανότητα"]]

    # =========================
    # OPENING AREAS + LOSSES
    # =========================
    συνολικά_ανοίγματα = (
        d["μεγάλα_παράθυρα"] * ΑΝΟΙΓΜΑΤΑ["μεγάλο_παράθυρο"] +
        d["μικρά_παράθυρα"] * ΑΝΟΙΓΜΑΤΑ["μικρό_παράθυρο"] +
        d["μπαλκονόπορτες"] * ΑΝΟΙΓΜΑΤΑ["μπαλκονόπορτα"] +
        d["μονές_πόρτες"] * ΑΝΟΙΓΜΑΤΑ["μονή_πόρτα"] +
        d["διπλές_συρόμενες"] * ΑΝΟΙΓΜΑΤΑ["διπλή_συρόμενη_μπαλκονόπορτα"]
    )

    # -------------------------
    # 1. CONDUCTION THROUGH OPENINGS (FIX #1)
    # -------------------------
    u_loss_openings = 0

    u_loss_openings += d["μεγάλα_παράθυρα"] * ΑΝΟΙΓΜΑΤΑ["μεγάλο_παράθυρο"] * U_ΑΝΟΙΓΜΑΤΩΝ["μεγάλο_παράθυρο"]
    u_loss_openings += d["μικρά_παράθυρα"] * ΑΝΟΙΓΜΑΤΑ["μικρό_παράθυρο"] * U_ΑΝΟΙΓΜΑΤΩΝ["μικρό_παράθυρο"]
    u_loss_openings += d["μπαλκονόπορτες"] * ΑΝΟΙΓΜΑΤΑ["μπαλκονόπορτα"] * U_ΑΝΟΙΓΜΑΤΩΝ["μπαλκονόπορτα"]
    u_loss_openings += d["μονές_πόρτες"] * ΑΝΟΙΓΜΑΤΑ["μονή_πόρτα"] * U_ΑΝΟΙΓΜΑΤΩΝ["μονή_πόρτα"]
    u_loss_openings += d["διπλές_συρόμενες"] * ΑΝΟΙΓΜΑΤΑ["διπλή_συρόμενη_μπαλκονόπορτα"] * U_ΑΝΟΙΓΜΑΤΩΝ["διπλή_συρόμενη_μπαλκονόπορτα"]

    u_loss_openings *= abs_dt * 1.2

    # -------------------------
    # 2. INFILTRATION (FIX #2)
    # -------------------------
    infiltration = (
        d["μεγάλα_παράθυρα"] * ΔΙΑΡΡΟΕΣ_ΑΝΑ_ΑΝΟΙΓΜΑ["μεγάλο_παράθυρο"] +
        d["μικρά_παράθυρα"] * ΔΙΑΡΡΟΕΣ_ΑΝΑ_ΑΝΟΙΓΜΑ["μικρό_παράθυρο"] +
        d["μπαλκονόπορτες"] * ΔΙΑΡΡΟΕΣ_ΑΝΑ_ΑΝΟΙΓΜΑ["μπαλκονόπορτα"] +
        d["μονές_πόρτες"] * ΔΙΑΡΡΟΕΣ_ΑΝΑ_ΑΝΟΙΓΜΑ["μονή_πόρτα"] +
        d["διπλές_συρόμενες"] * ΔΙΑΡΡΟΕΣ_ΑΝΑ_ΑΝΟΙΓΜΑ["διπλή_συρόμενη_μπαλκονόπορτα"]
    )

    infiltration *= abs_dt * 0.8

