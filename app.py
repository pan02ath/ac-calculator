# =========================================================
# ENGINE
# =========================================================
def υπολογισμός(d, mode):
    επιφάνεια = d["επιφάνεια"]
    ύψος      = d["ύψος"]
    ΔΤ        = abs(d["εξωτερική"] - d["εσωτερική"])
    όγκος     = επιφάνεια * ύψος

    περίμετρος  = 4 * math.sqrt(επιφάνεια)
    A_ολοτοίχου = περίμετρος * ύψος

    opening_map = [
        ("μεγάλα",         "μεγάλο_παράθυρο"),
        ("μικρά",          "μικρό_παράθυρο"),
        ("μπαλκονόπορτες", "διπλή_ανοιγόμενη_μπαλκονόπορτα"),
        ("μονές",          "μονή_ανοιγόμενη_μπαλκονόπορτα"),
        ("συρόμενες",      "διπλή_συρόμενη_μπαλκονόπορτα"),
    ]

    A_ανοιγμάτων_total = sum(d[k] * ΑΝΟΙΓΜΑΤΑ[key] for k, key in opening_map)
    A_τοίχου_net       = max(A_ολοτοίχου - A_ανοιγμάτων_total, 0)

    U_wall       = U_ΤΟΙΧΟΥ[(d["έτος"], d["μόνωση"])]
    u_factor     = ΚΟΥΦΩΜΑΤΑ_U[d["κουφώματα"]]
    shgc_val     = SHGC[d["κουφώματα"]]
    solar_orient = ΠΡΟΣΑΝΑΤΟΛΙΣΜΟΣ[d["προσανατολισμός"]]
    shading      = ΣΚΙΑΣΗ[d["σκίαση"]]

    # 1. WALL CONDUCTION
    Q_τοίχοι = (
        U_wall * A_τοίχου_net * ΔΤ
        * ΤΟΙΧΟΙ[d["εξωτερικοί"]]
        * ΜΗ_ΘΕΡΜΑΙΝΟΜΕΝΟΙ[d["μη_θερμαινόμενοι"]]
    )

    # 2. FLOOR CONDUCTION
    Q_δάπεδο = U_wall * 0.6 * επιφάνεια * ΔΤ * ΔΑΠΕΔΟ[d["δάπεδο"]]

    # 3. ROOF — conduction + solar gain
    Q_οροφή        = 0.0
    Q_οροφή_ηλιακά = 0.0
    if d["οροφή_υπάρχει"]:
        roof_type       = d["οροφή"]
        Q_οροφή         = U_wall * 0.8 * επιφάνεια * ΔΤ * ΟΡΟΦΗ_U[roof_type]
        # Solar gain through roof: G_horiz × absorption_fraction per roof type
        Q_οροφή_ηλιακά  = επιφάνεια * ΗΛΙΑΚΗ_ΟΡΙΖΟΝΤΙΑ * ΟΡΟΦΗ_ΗΛΙΑΚΟ[roof_type]

    # 4. OPENINGS — conduction + infiltration + solar gain through glass
    Q_ανοίγματα = 0.0
    Q_διαρροές  = 0.0
    Q_ηλιακά    = 0.0
    for k, key in opening_map:
        count = d[k]
        if count == 0:
            continue
        area = count * ΑΝΟΙΓΜΑΤΑ[key]
        Q_ανοίγματα += area * U_ΑΝΟΙΓΜΑΤΩΝ[key] * u_factor * ΔΤ
        Q_διαρροές  += count * ΔΙΑΡΡΟΕΣ[key] * ΑΕΡΟΔΙΕΙΣΔΥΣΗ[d["αεροστεγανότητα"]] * ΔΤ
        # Solar: glass area × peak vertical irradiance × SHGC × orientation × shading
        Q_ηλιακά    += area * FRAME_FACTOR * ΗΛΙΑΚΗ_ΚΑΘΕΤΗ * shgc_val * solar_orient * shading

    # 5. VENTILATION
    Q_αερισμός = ACH[d["αερισμός"]] * όγκος * 0.34 * ΔΤ

    # 6. INTERNAL LOADS
    Q_εσωτερικά = ΕΣΩΤΕΡΙΚΑ[d["τύπος"]]

    # 7. LATENT LOAD (+12% cooling only — Mediterranean humidity)
    latent = 1.12 if mode == "cooling" else 1.0

    if mode == "cooling":
        Q_total = (
            Q_τοίχοι + Q_δάπεδο + Q_οροφή
            + Q_ανοίγματα + Q_διαρροές
            + Q_ηλιακά + Q_οροφή_ηλιακά
            + Q_αερισμός + Q_εσωτερικά
        ) * latent
    else:
        Q_total = max(
            Q_τοίχοι + Q_δάπεδο + Q_οροφή
            + Q_ανοίγματα + Q_διαρροές + Q_αερισμός
            - Q_ηλιακά - Q_οροφή_ηλιακά - Q_εσωτερικά,
            0
        )

    kw  = Q_total / 1000
    btu = kw * 3412

    btu_margin      = btu * 1.10
    συνιστώμενο_btu = next((b for b in ΜΕΓΕΘΗ_BTU if b >= btu_margin), ΜΕΓΕΘΗ_BTU[-1])
    συνιστώμενο_kw  = συνιστώμενο_btu / 3412

    breakdown = {
        "Τοίχοι (αγωγή)":        Q_τοίχοι,
        "Δάπεδο (αγωγή)":        Q_δάπεδο,
        "Οροφή (αγωγή)":         Q_οροφή,
        "Οροφή (ηλιακό)":        Q_οροφή_ηλιακά,
        "Ανοίγματα (αγωγή)":     Q_ανοίγματα,
        "Διαρροές / Αεροδ.":     Q_διαρροές,
        "Αερισμός":               Q_αερισμός,
        "Ηλιακά κέρδη (τζάμια)": Q_ηλιακά,
        "Εσωτερικά κέρδη":        Q_εσωτερικά,
    }

    return kw, btu, συνιστώμενο_kw, συνιστώμενο_btu, breakdown, Q_total


