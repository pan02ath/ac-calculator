import streamlit as st

# -----------------------------
# Τυπικά ανοίγματα (m²)
# -----------------------------
ΑΝΟΙΓΜΑΤΑ = {
    "μονή_πόρτα": 0.90 * 2.10,
    "διπλή_πόρτα": 1.40 * 2.10,
    "μπαλκονόπορτα": 1.60 * 2.10,
    "μεγάλο_παράθυρο": 1.20 * 1.40,
    "μικρό_παράθυρο": 0.60 * 0.60,
}

# -----------------------------
# Συντελεστές περιβάλλοντος
# -----------------------------
ΗΛΙΟΣ = {
    "σκιασμένο": 0.85,
    "κανονικό": 1.0,
    "έντονη": 1.2
}

ΑΕΡΟΣΤΕΓΑΝΟΤΗΤΑ = {
    "καλή": 0.9,
    "μέτρια": 1.0,
    "κακή": 1.15
}

ΣΤΕΓΗ = {
    "όχι": 1.0,
    "ναι": 1.2
}

ΠΟΙΟΤΗΤΑ_ΚΤΙΡΙΟΥ = {
    "παλιό": 1.25,
    "μέσο": 1.0,
    "μοντέρνο": 0.85,
    "υψηλής_απόδοσης": 0.7
}

# -----------------------------
# ΝΕΟ: Κουφώματα (πολύ σημαντικό)
# -----------------------------
ΚΟΥΦΩΜΑΤΑ = {
    "αλουμίνιο_μονό": 1.35,        # χειρότερο σενάριο
    "αλουμίνιο_θερμοδιακοπή": 1.0, # βασικό ελληνικό standard
    "pvc_διπλό": 0.85,             # καλύτερο
    "premium_low_e": 0.7           # υψηλής απόδοσης
}

# -----------------------------
# Υπολογισμός φορτίου
# -----------------------------
def υπολογισμός(d):
    επιφάνεια = d["επιφάνεια"]
    ύψος = d["ύψος"]
    όγκος = επιφάνεια * ύψος

    delta_t = d["εξωτερική"] - d["εσωτερική"]
    delta_t = max(delta_t, 5)

    # βασική θερμική φόρτιση
    συντελεστής = 30 + (delta_t - 10) * 1.2
    συντελεστής = max(25, συντελεστής)

    βασικό = όγκος * συντελεστής

    # -----------------------------
    # Ανοίγματα
    # -----------------------------
    παράθυρα = (
        d["μεγάλα_παράθυρα"] * ΑΝΟΙΓΜΑΤΑ["μεγάλο_παράθυρο"] +
        d["μικρά_παράθυρα"] * ΑΝΟΙΓΜΑΤΑ["μικρό_παράθυρο"] +
        d["μπαλκονόπορτες"] * ΑΝΟΙΓΜΑΤΑ["μπαλκονόπορτα"]
    )

    πόρτες = (
        d["μονές_πόρτες"] * ΑΝΟΙΓΜΑΤΑ["μονή_πόρτα"] +
        d["διπλές_πόρτες"] * ΑΝΟΙΓΜΑΤΑ["διπλή_πόρτα"]
    )

    συνολικά_ανοίγματα = παράθυρα + πόρτες

    # -----------------------------
    # Ηλιακά + απώλειες κουφωμάτων
    # -----------------------------
    ηλιακά = συνολικά_ανοίγματα * 260
    ηλιακά *= ΗΛΙΟΣ[d["ήλιος"]]

    # Εδώ μπαίνει το νέο σημαντικό factor
    ηλιακά *= ΚΟΥΦΩΜΑΤΑ[d["κουφώματα"]]

    # -----------------------------
    # Συντελεστές κτιρίου
    # -----------------------------
    βασικό *= ΑΕΡΟΣΤΕΓΑΝΟΤΗΤΑ[d["αεροστεγανότητα"]]
    βασικό *= ΣΤΕΓΗ[d["στέγη"]]
    βασικό *= ΠΟΙΟΤΗΤΑ_ΚΤΙΡΙΟΥ[d["ποιότητα"]]

    συνολικό = βασικό + ηλιακά

    kw = συνολικό / 1000
    btu = συνολικό * 3.412

    return kw, btu, συνολικά_ανοίγματα


# -----------------------------
# UI
# -----------------------------
import streamlit as st

st.title("Υπολογιστής Κλιματιστικού Δωματίου")

st.header("1. Διαστάσεις")
επιφάνεια = st.number_input("Επιφάνεια (m²)", 5.0, 200.0, 20.0)
ύψος = st.number_input("Ύψος (m)", 2.0, 4.5, 2.8)

st.header("2. Θερμοκρασίες")
εσωτερική = st.number_input("Εσωτερική θερμοκρασία (°C)", 20, 28, 24)
εξωτερική = st.number_input("Εξωτερική θερμοκρασία (°C)", 30, 45, 35)

st.header("3. Ανοίγματα")

μεγάλα_παράθυρα = st.number_input("Μεγάλα παράθυρα", 0, 20, 2)
μικρά_παράθυρα = st.number_input("Μικρά παράθυρα", 0, 20, 1)
μπαλκονόπορτες = st.number_input("Μπαλκονόπορτες", 0, 5, 0)

μονές_πόρτες = st.number_input("Μονές πόρτες", 0, 5, 1)
διπλές_πόρτες = st.number_input("Διπλές πόρτες", 0, 5, 0)

st.header("4. Περιβάλλον")

ήλιος = st.selectbox("Έκθεση στον ήλιο", ["σκιασμένο", "κανονικό", "έντονη"])
αεροστεγανότητα = st.selectbox("Αεροστεγανότητα", ["καλή", "μέτρια", "κακή"])
στέγη = st.selectbox("Κάτω από στέγη;", ["όχι", "ναι"])
ποιότητα = st.selectbox("Ποιότητα κτιρίου", ["παλιό", "μέσο", "μοντέρνο", "υψηλής_απόδοσης"])

st.header("5. Κουφώματα (παράθυρα & μπαλκονόπορτες)")
κουφώματα = st.selectbox(
    "Τύπος κουφωμάτων",
    [
        "αλουμίνιο_μονό",
        "αλουμίνιο_θερμοδιακοπή",
        "pvc_διπλό",
        "premium_low_e"
    ]
)

# -----------------------------
# Data
# -----------------------------
d = {
    "επιφάνεια": επιφάνεια,
    "ύψος": ύψος,
    "εσωτερική": εσωτερική,
    "εξωτερική": εξωτερική,
    "μεγάλα_παράθυρα": μεγάλα_παράθυρα,
    "μικρά_παράθυρα": μικρά_παράθυρα,
    "μπαλκονόπορτες": μπαλκονόπορτες,
    "μονές_πόρτες": μονές_πόρτες,
    "διπλές_πόρτες": διπλές_πόρτες,
    "ήλιος": ήλιος,
    "αεροστεγανότητα": αεροστεγανότητα,
    "στέγη": στέγη,
    "ποιότητα": ποιότητα,
    "κουφώματα": κουφώματα
}

# -----------------------------
# Output
# -----------------------------
if st.button("Υπολογισμός"):
    kw, btu, a = υπολογισμός(d)

    st.subheader("Αποτέλεσμα")

    st.write(f"Συνολικά ανοίγματα: {a:.2f} m²")
    st.write(f"Ψυκτικό φορτίο: {kw:.2f} kW")
    st.write(f"Ψυκτικό φορτίο: {btu:.0f} BTU/h")

    if kw < 2.5:
        st.success("Προτεινόμενο: ~9.000 BTU")
    elif kw < 3.5:
        st.success("Προτεινόμενο: ~12.000 BTU")
    elif kw < 5:
        st.success("Προτεινόμενο: ~18.000 BTU")
    else:
        st.success("Προτεινόμενο: 24.000+ BTU")
