def υπολογισμός(d, mode):

    volume = d["επιφάνεια"] * d["ύψος"]
    ΔΤ = abs(d["εξωτερική"] - d["εσωτερική"])

    floor_area = max(d["επιφάνεια"], 1)
    side = math.sqrt(floor_area)

    total_wall_area = 4 * side * d["ύψος"]
    wall_area = total_wall_area * ΤΟΙΧΟΙ[d["εξωτερικοί"]]

    roof_area = floor_area if d["οροφή_υπάρχει"] else 0

    # ---------------- U-values (PURE) ----------------
    U_wall = U_ΤΟΙΧΟΥ[d["μόνωση"]] * ΜΗ_ΘΕΡΜΑΙΝΟΜΕΝΟΙ.get(d["μη_θερμαινόμενοι"], 1.0)
    U_roof = U_ΟΡΟΦΗΣ_BASE[d["μόνωση"]] * ΟΡΟΦΗ.get(d["οροφή"], 1.0) if d["οροφή_υπάρχει"] else 0
    U_floor = U_ΔΑΠΕΔΟΥ_BASE[d["μόνωση"]] * ΔΑΠΕΔΟ[d["δάπεδο"]]
    U_win = ΚΟΥΦΩΜΑΤΑ[d["κουφώματα"]]

    # ---------------- b-factors ONLY ----------------
    b_roof = ADJACENCY_B.get(d["οροφή"], 1.0) if d["οροφή_υπάρχει"] else 0
    b_floor = ADJACENCY_B.get(d["δάπεδο"], 1.0)
    b_wall = ADJACENCY_B.get(
        "μη_θερμαινόμενος_χώρος" if d["μη_θερμαινόμενοι"] > 0 else "άλλο_διαμέρισμα",
        1.0
    )

    # ---------------- WINDOWS ----------------
    window_loss = 0
    solar_gain = 0
    glazing_area = 0

    windows = [
        (d["μεγάλα"], "μεγάλο_παράθυρο"),
        (d["μικρά"], "μικρό_παράθυρο"),
        (d["μπαλκονόπορτες"], "διπλή_ανοιγόμενη_μπαλκονόπορτα"),
        (d["μονές"], "μονή_ανοιγόμενη_μπαλκονόπορτα"),
        (d["συρόμενες"], "διπλή_συρόμενη_μπαλκονόπορτα"),
    ]

    for count, key in windows:
        area = count * ΑΝΟΙΓΜΑΤΑ[key]
        glazing_area += area
        window_loss += U_win * area * ΔΤ

        if mode == "ψύξη":
            solar_gain += area * d["βάση_ακτινοβολίας"] * d["ηλιακή_έκθεση"] * 0.35

    roof_solar = 0
    if mode == "ψύξη" and d["οροφή_υπάρχει"]:
        coeff = {"ταράτσα_εκτεθειμένη": 35, "μονωμένη": 15, "κεραμοσκεπή": 22, "θερμαινόμενος_χώρος": 0}
        roof_solar = roof_area * coeff.get(d["οροφή"], 0) * d["ηλιακή_έκθεση"]

    effective_wall_area = max(wall_area - glazing_area, 0)

    internal = ΕΣΩΤΕΡΙΚΑ[d["τύπος"]] * ΕΣΩΤΕΡΙΚΑ_ΣΥΝΤΕΛΕΣΤΗΣ[d["τύπος"]]

    # ---------------- TRANSMISSION ----------------
    transmission = (
        U_wall * effective_wall_area * b_wall * ΔΤ +
        U_roof * roof_area * b_roof * ΔΤ +
        U_floor * floor_area * b_floor * ΔΤ +
        window_loss
    )

    # explicit (not hidden physics)
    thermal_bridge = 0.10 if U_wall <= 0.55 else 0.05
    transmission *= (1 + thermal_bridge)

    # ---------------- INFILTRATION ----------------
    ACH = ΑΕΡΟΔΙΕΙΣΔΥΣΗ[d["αεροστεγανότητα"]]
    infiltration = 0.33 * ACH * volume * ΔΤ

    # ---------------- TOTAL ----------------
    if mode == "θέρμανση":
        total = (transmission + infiltration) * 1.10
    else:
        total = transmission + infiltration + solar_gain + roof_solar + internal

    total = max(total, 0)

    load_btu = total * 3.412

    # ---------------- DERATING ----------------
    temp = d["εξωτερική"]

    if mode == "θέρμανση":
        if temp >= 7: f = 1.00
        elif temp >= 4: f = 0.96
        elif temp >= 0: f = 0.88
        elif temp >= -7: f = 0.75
        else: f = 0.65
    else:
        if temp <= 35: f = 1.00
        elif temp <= 40: f = 0.97
        else: f = 0.90

    nominal_base = load_btu / f
    nominal_final = nominal_base

    penalties = {}

    if d.get("περιστασιακή"):
        nominal_final *= 1.25
        penalties["Περιστασιακή χρήση"] = 1.25

    if d.get("αθόρυβη"):
        nominal_final *= 1.20
        penalties["Αθόρυβη/χαμηλή ταχύτητα"] = 1.20

    breakdown = {
        "Τοίχοι": U_wall * effective_wall_area * ΔΤ,
        "Οροφή": U_roof * roof_area * b_roof * ΔΤ,
        "Δάπεδο": U_floor * floor_area * b_floor * ΔΤ,
        "Ανοίγματα": window_loss,
        "Αεροδιείσδυση": infiltration,
        "Ήλιος": solar_gain + roof_solar if mode == "ψύξη" else 0,
        "Εσωτερικά": internal if mode == "ψύξη" else 0,
    }

    return total / 1000, load_btu, nominal_base, nominal_final, penalties, breakdown, f
