# =========================================================
# ENGINE (CLEAN EN12831 STRUCTURE)
# =========================================================

def υπολογισμός(d, mode):

    volume = d["επιφάνεια"] * d["ύψος"]
    ΔΤ = abs(d["εξωτερική"] - d["εσωτερική"])

    floor_area = max(d["επιφάνεια"], 1)
    side = math.sqrt(floor_area)

    total_wall_area = 2 * d["ύψος"] * side * 2
    wall_area = total_wall_area * ΤΟΙΧΟΙ[d["εξωτερικοί"]]
    roof_area = floor_area if d["οροφή_υπάρχει"] else 0

    # =====================================================
    # U VALUES (PURE)
    # =====================================================
    U_wall = U_ΤΟΙΧΟΥ[d["μόνωση"]]
    U_roof = U_ΟΡΟΦΗΣ_BASE[d["μόνωση"]] if d["οροφή_υπάρχει"] else 0
    U_floor = U_ΔΑΠΕΔΟΥ_BASE[d["μόνωση"]]
    U_win = ΚΟΥΦΩΜΑΤΑ[d["κουφώματα"]]

    # =====================================================
    # B FACTORS (ONLY PLACE ADJACENCY IS HANDLED)
    # =====================================================
    b_wall = B_FACTORS.get(
        "μη_θερμαινόμενος_χώρος" if d.get("μη_θερμαινόμενοι", 0) > 0 else "εξωτερικός_αέρας",
        1.0
    )

    b_roof = B_FACTORS.get(d["οροφή"], 1.0) if d["οροφή_υπάρχει"] else 0
    b_floor = B_FACTORS.get(d["δάπεδο"], 1.0)

    # =====================================================
    # WINDOWS
    # =====================================================
    window_loss = 0
    total_glazing_area = 0

    windows = [
        (d["μεγάλα"], "μεγάλο_παράθυρο"),
        (d["μικρά"], "μικρό_παράθυρο"),
        (d["μπαλκονόπορτες"], "διπλή_ανοιγόμενη_μπαλκονόπορτα"),
        (d["μονές"], "μονή_ανοιγόμενη_μπαλκονόπορτα"),
        (d["συρόμενες"], "διπλή_συρόμενη_μπαλκονόπορτα"),
    ]

    for count, key in windows:
        area = count * ΑΝΟΙΓΜΑΤΑ[key]
        total_glazing_area += area
        window_loss += U_win * area * ΔΤ

    # =====================================================
    # TRANSMISSION (EN12831 CORE FORMULA)
    # =====================================================
    transmission = (
        U_wall * wall_area * ΔΤ * b_wall
        + U_roof * roof_area * ΔΤ * b_roof
        + U_floor * floor_area * ΔΤ * b_floor
        + window_loss
    )

    # =====================================================
    # INFILTRATION (PURE ACH MODEL)
    # =====================================================
    ACH = ΑΕΡΟΔΙΕΙΣΔΥΣΗ[d["αεροστεγανότητα"]]
    infiltration = 0.33 * ACH * volume * ΔΤ

    # =====================================================
    # TOTAL LOAD
    # =====================================================
    if mode == "θέρμανση":
        total = transmission + infiltration
        if d.get("βόρειος"):
            total *= 1.10
    else:
        total = transmission + infiltration

    total = max(total, 0)
    load_btu = total * 3.412

    # =====================================================
    # UNIT SIZING
    # =====================================================
    f_derating = 1.0
    temp = d["εξωτερική"]

    if mode == "θέρμανση":
        if temp >= 7: f_derating = 1.00
        elif temp >= 4: f_derating = 0.96
        elif temp >= 0: f_derating = 0.88
        elif temp >= -7: f_derating = 0.75
        else: f_derating = 0.65
    else:
        if temp <= 35: f_derating = 1.00
        elif temp <= 40: f_derating = 0.97
        else: f_derating = 0.90

    nominal_btu = load_btu / f_derating

    return total / 1000, load_btu, nominal_btu, nominal_btu, {}, {}, f_derating
