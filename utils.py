def get_commercial_range(nominal_btu_val):
    sizes = [7, 9, 10, 12, 13, 14, 16, 18, 20, 22, 24, 30]
    val_k = nominal_btu_val / 1000

    for s in sizes:
        if s >= val_k:
            return f"{s*1000:,}"

    return f"{sizes[-1]*1000:,}+"


